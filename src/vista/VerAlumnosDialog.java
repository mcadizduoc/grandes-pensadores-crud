    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int selectedAlumnoIndex = this.tblAlumnos.getSelectedRow();
        
        if (selectedAlumnoIndex > -1) {
            Alumno a = this.alumnos.get(selectedAlumnoIndex);
            Alumno.deleteAlumno(a.getId());
            
            this.cargarAlumnos();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un alumno antes de eliminarlo");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int selectedAlumnoIndex = this.tblAlumnos.getSelectedRow();
        
        if (selectedAlumnoIndex > -1) {
            Alumno alumno = this.alumnos.get(selectedAlumnoIndex);
            
            EditarDialog ed = new EditarDialog((Frame)this.getParent(), true, alumno, this);
            ed.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un alumno antes de editarlo");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

