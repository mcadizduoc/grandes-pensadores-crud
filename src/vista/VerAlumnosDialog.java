    public void cargarAlumnos() {
        this.alumnos = this.controller.getAlumnos();
        
        DefaultTableModel model = (DefaultTableModel)this.tblAlumnos.getModel();
        
        model.setRowCount(0);
        for (Alumno a : this.alumnos) {
            model.addRow(new Object[] { a.getNombreCompleto(), a.getNombreApoderado(), a.getFechaNacimiento().format(DateTimeFormatter.ISO_DATE), a.getCurso().getNombreCurso() });
        }
    }

